			<h2>Spielablauf</h2>

			<p>Der Spielablauf ist in Runden gegliedert, wobei die beiden spielenden Mannschaften abwechselnd am Zug sind. Durch „Schere, Stein, Papier“ wird vor dem Spiel, durch je einen Vertreter der beiden Mannschaften entschieden, welche Mannschaft beginnen darf.</p>

