			<p>Ursprünglich wurde Flunkyball, wie der Name vermuten lässt, mit einem Ball gespielt. Aus seiner Natur heraus, vor allem spontan gespielt zu werden, und weil man nunmal nicht ständig spontan einen Ball dabei hat, hat sich in vielen Kreisen etabliert den Ball durch eine Flasche zu ersetzten.</p>

