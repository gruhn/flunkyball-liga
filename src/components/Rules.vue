			<h2>Spielaufbau</h2>

			<p>Ein Liga-konformes Flunkyball-Spiel wird mit zwei, gegeneinander antretenden, Mannschaften mit je fünf Spielern ausgefochten. Jeder Spieler ist mit einer 0,5 Liter Bierflasche zum Trinken ausgestattet. Das Bier sollte wenigstens durchschnittlichen Alkoholgehalt aufweisen (~ 4,8%).</p>

