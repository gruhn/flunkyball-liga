			<p>Die erste der beiden Mannschaften gewinnt das Spiel, in der alle Spieler ihre Trinkflasche vollständig ausgetrunken haben.</p>


			<h2>Regelverstöße und Ausnahmen</h2>

			<p>Die meisten Regelverstöße werden mit so genannten Strafschlücken<sup>1</sup> geahndet. Dabei erhalten die Spieler der, durch das Foul geschädigten, Mannschaft die Möglichkeit eine kurze Zeit lang aus ihren Trinkfalschen zu trinken, obwohl sie keinen Treffer gelandet haben. </p>

			<p>Wird ein Strafschluck zugunsten einer Mannschaft genemigt<sup>2</sup>, fordert der Schiedsrichter oder ein unparteiischer Dritter, die Spieler der Mannschaft, auf ihre Trinkflaschen anzusetzten. Nun zählt er zwei mal mit gleichmäßiger Geschwindigkeit von eins bis drei. Nach dem ersten mal Zählen fangen die Spieler an zu trinken. Nach dem zweiten Mal müssen sie wieder aufhören.</p>

			<p>Das einleitende Zählen soll den Spielern ein Gefühl für die Zeit geben, die sie zu trinken haben, und verhindern, dass jemand unachtsam seine Chance zum Trinken verpasst.</p>

