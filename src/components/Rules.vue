			<p>Schlägt der Wurf fehl, ist die Runde damit beendet und die gegnerische Mannschaft ist am Zug. Ein Wurf gilt auch als fehlgeschlagen, wenn die Standflasche zwar getroffen aber nicht umgestoßen wurde. Ist der Wurf ein Treffer, dürfen alle Spieler der Mannschaft des Werfers anfangen aus ihren Bierflaschen zu trinken. Zwei Läufer der gegnerischen Mannschaft müssen währenddessen so schnell wie möglich die umgestoßene Standflasche wieder auf ihrer ursprünglichen Position in der Mitte des Spielfeldes aufstellen und zusammen mit der Wurfflasche zurück zur Basislinie ihrer Mannschaft rennen. Das überschreiten der Basislinie muss der Werfermannschaft durch lautes „Stopp!“-Zurufen signalisiert werden, denn ab diesem Moment darf nicht weiter getrunken werden.</p>

			<p>Die Rolle der Läufer ist frei bestimmt und muss, nicht wie der Werfer, abgewechselt werden.</p>

